package frc.robot.commands.teleop;

import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.IndexerTowerSubsystem;

public class ToggleIndexerTower extends CommandBase {
  private boolean button;
  private boolean on;

  public ToggleIndexerTower() {
    addRequirements(IndexerTowerSubsystem.getInstance());
    button = true;
  }

  public ToggleIndexerTower(boolean on) {
    addRequirements(IndexerTowerSubsystem.getInstance());
    button = false;
    this.on = on;
  }

  @Override
  public void execute() {
    if (button) {
      IndexerTowerSubsystem.getInstance().toggle(true);
    } else {
      IndexerTowerSubsystem.getInstance().toggle(on);
    }
  }

  @Override
  public void end(boolean interrupted) {
    if (button) {
      IndexerTowerSubsystem.getInstance().toggle(false);
    }
  }

  @Override
  public boolean isFinished() {
    return !button;
  }
}
